

beta = 0.97;
Y(1:10000) = 0;
Y(1:10) = [257, 658, 756, 987, 1987, 1879, 1465, 546, 354, 256];
YA(11:10000) = 180;
YB(11:10000) = 150;
r = 0.0235;
R = 1 + r;
PVY = 0;
q = 0.23;
for i= 1:10
    PVY = PVY + Y(i)/(R^i);
end

PVY = PVY + q* (YA(11) / R^10) /(1-1/R) + (1-q)*(YB(11)/R^10)/(1-1/R);

C(1) = PVY * (1 - beta);
B(1) = 0;
for i = 2:10
    C(i) = beta * (1+r) * C(i-1);
    B(i) = Y(i-1) + (1 + r) * B(i-1) - C(i-1);
end

Y(11:10000) = 170;
for i = 11:10000
    C(i) = beta * R * C(i-1);
    B(i) = Y(i-1) + R*B(i-1) - C(i-1);
end